public class swapping{
    public static void main(String []args){
        System.out.println("swapping in java");
        int a=10;
        int b=20;
         System.out.println("Before swapping");
          System.out.println(a+" "+b);
        int temp=a;
        a=b;
        b=temp;
         System.out.println("After swapping");
          System.out.println(a+" "+b);

    }
}